int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	unsigned char i;

	i = 0;
	(unsigned char)n;
	while (s1[i] != '\0' && s2[i] != '\0' && i < n)
	{
		if (s1[i] != s2[i])
			return (s1[i] - s2[i]);
		i++;
	}
	return (s1[i] - s2[i]);
}
