int		ft_sqrt(int nb)
{
	int	nr;
	int	sqrt;

	nr = 1;
	if (nb == 1)
		return (nb);
	while (nr < nb / 2)
	{
		sqrt = nr * nr;
		if (sqrt == nb)
			return (nr);
		nr++;
	}
	return (0);
}
