#include "libft.h"
#include "fillit.h"

void	ft_disp(char *s, int n)
{
	int		i;

	i = 0;
	while (i < (n - 1) * 42 + n)
	{
		if (!((i - n) % 42) && i >= n)
		{
			i = i - n + 42;
			ft_putchar('\n');
		}
		ft_putchar(s[i++]);
	}
	ft_putchar('\n');
}
